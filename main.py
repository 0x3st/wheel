# 控制台打印爱心像素画
# 可调参数：
#   SCALE  放大倍数（整数，>=1）
#   FILL   填充字符（如 '█'、'❤'、'#'）
#   EMPTY  空白字符（如 ' '）
SCALE = 2
FILL  = '#'
EMPTY = ' '

# 1=填充，0=空
heart = [
    "001100001100",
    "011110011111",
    "111111111111",
    "111111111111",
    "011111111110",
    "001111111100",
    "000111111000",
    "000011110000",
    "000001100000",
    "000000000000",
]

for row in heart:
    # 横向放大
    line = ''.join((FILL if c == '1' else EMPTY) * SCALE for c in row)
    # 纵向放大
    for _ in range(SCALE):
        print(line)
